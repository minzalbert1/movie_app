class Movie {
  String title;
  String imageUrl;
  String description;
  String genre;
  String director;
  int duration;
  int releaseYear;
  double rating;

  Movie({
    required this.title,
    required this.imageUrl,
    required this.description,
    required this.genre,
    required this.director,
    required this.duration,
    required this.releaseYear,
    required this.rating,
  });
}
